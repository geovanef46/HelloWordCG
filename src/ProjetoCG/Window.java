    public void iniciaPlanoXY(GL2 gl){
      //plano X
      gl.glBegin (GL2.GL_LINES);
      gl.glVertex3f(1f,0, 0);
      gl.glVertex3f(-1f,0, 0);
      gl.glEnd();
      //plano Y
      gl.glBegin (GL2.GL_LINES);
      gl.glVertex3f(0,-1f, 0);
      gl.glVertex3f(0, 1f, 0);
      gl.glEnd();
    }

 public static int posicionaNoPlanoX(int x) {
        return x - 250;
    }

    public static int posicionaNoPlanoY(int y) {
     
        return 250 - y;
    }
    
    
    
    
