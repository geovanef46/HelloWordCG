    private void gLCanvasMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gLCanvasMouseMoved
                valorx = posicionaNoPlanoX(evt.getX());
                valory = posicionaNoPlanoY(evt.getY());
                lblX.setText(Integer.toString(valorx));
                lblY.setText(Integer.toString(valory));
                lblNdcx.setText(String.format("%.3f", (converte.paraNDCX(valorx))));
                lblNdcy.setText(String.format("%.3f", (converte.paraNDCY(valory))));
                lblDcx.setText(Integer.toString(converte.paraNCX()));
                lblDcy.setText(Integer.toString(converte.paraNCY()));
    }//GEN-LAST:event_gLCanvasMouseMoved

    private void gLCanvasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gLCanvasMouseClicked
      ArrayCoord[i] = (float) converte.paraNDCX(evt.getX());
      i++;
      ArrayCoord[i] = (float) converte.paraNDCY(evt.getY());
      i++;
      ArrayCoord[i] = 0;
      i = 0;
      clicked = true;
    }//GEN-LAST:event_gLCanvasMouseClicked

